function compareStrings(str1, str2) {
    // Compare the strings
    if (str1 === str2) {
        console.log("The strings are equal.");
    } else {
        console.log("The strings are not equal.");
    }
}

let string1 = "Hello, World!";
let string2 = "Hello, world!"; 

compareStrings(string1, string2);
